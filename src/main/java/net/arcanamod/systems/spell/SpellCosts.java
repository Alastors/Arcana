package net.arcanamod.systems.spell;

public class SpellCosts {
    int air;
    int water;
    int fire;
    int earth;
    int order;
    int chaos;
    int special;

    public SpellCosts(int air, int water, int fire, int earth, int order, int chaos, int special){
        this.air = air;
        this.water = water;
        this.fire = fire;
        this.earth = earth;
        this.order = order;
        this.chaos = chaos;
        this.special = special;
    }
}
